#include <iostream>
#include <stdio.h>

#define MAX 20001

using namespace std;

int T;
int n;
long long *a;

void input() {
	cin >> n;
	a = new long long[n+1];
	for(int i = 1; i <= n; i++) {
		cin >> a[i];
	}
}

int max(int a, int b) {
	return a>b?a:b;
}

int arrayGame(int s, int e) {
	if(s == e) {
		return 0;
	}

	long long sum = 0;
	for(int i = s; i <= e; i++) {
		sum += a[i];
	}

	if(sum == 0) {
		return e-s;
	}

	//tổng là số lẻ thì ko thể chia
	if(sum % 2 != 0) {
		return 0;
	}

	long long temp = 0;
	int midIndex = -1;
	for(int i = s; i <= e; i++) {
		temp += a[i];
		if(temp == sum/2) {
			midIndex = i;
			break;
		}
	}

	if(midIndex == -1) return 0;
	return 1 + max(arrayGame(s, midIndex), arrayGame(midIndex+1, e));
}

int main() {
	freopen("ArrayGame.txt", "r", stdin);
	cin >> T;
	for(int tc = 1; tc <= T; tc++) {
		input();
		cout << arrayGame(1, n) << endl;
	}
}
